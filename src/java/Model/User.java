package Model;

/**
 *
 * @author ADMIN
 */
public class User {
    private String ID, name, phone, sex, email;

    public User() {
    }

    public User(String ID, String name, String phone, String sex, String email) {
        this.ID = ID;
        this.name = name;
        this.phone = phone;
        this.sex = sex;
        this.email = email;
    }

    public String getID() {
        return ID;
    }

    public void setID(String ID) {
        this.ID = ID;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getSex() {
        return sex;
    }

    public void setSex(String sex) {
        this.sex = sex;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }
    
}
